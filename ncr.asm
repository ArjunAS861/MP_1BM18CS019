;program to calculate ncr
.model small
.data
N equ 05H
R equ 02H
RES DW ?
.code
        mov ax,N
        mov bx,R
        MOV RES,0000
CALL NCR
MOV BX,RES
MOV AH,4CH
INT 21H
NCR PROC NEAR
CMP AX,BX
JE RES1
CMP BX,0000H
JE RES1
CMP BX,0001H
JE RESN
DEC AX
CMP BX,AX
JE INCR
PUSH AX
PUSH BX
CALL NCR
POP BX
POP AX
DEC BX
PUSH AX
PUSH BX
CALL NCR
POP BX
POP AX
RET
RES1: INC RES
RET
INCR: INC RES
RESN: ADD RES,AX
RET
NCR ENDP
END
